using System;

namespace CMDR
{
	public static class MathHelper
	{
		public static float Acos(float num)
        {
			return (float)Math.Acos(num);
        }

		public static float Asin(float num)
        {
			return (float)Math.Asin(num);
        }

		public static float Atan(float num)
        {
			return (float)Math.Atan(num);
        }

		public static float Cos(float num)
		{
			return (float)Math.Cos(num);
		}
		
		public static float Sin(float num)
		{
			return (float)Math.Sin(num);
		}
		
		public static float Tan(float num)
		{
			return (float)Math.Tan(num);
		}
		
		public static float Abs(float num)
		{
			return (float)Math.Abs(num);
		}
		
		public static float Min(float num1, float num2)
		{
			return (float)Math.Min(num1, num2);
		}
		
		public static float Max(float num1, float num2)
		{
			return (float)Math.Max(num1, num2);
		}
		
		public static float Sqrt(float num)
		{
			return (float)Math.Sqrt(num);
		}
	}
}